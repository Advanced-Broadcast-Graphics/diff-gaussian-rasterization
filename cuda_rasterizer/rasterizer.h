	template <typename T>
	size_t required(size_t P) {
		char* size = nullptr;
		T::fromChunk(size, P);
		return ((size_t)size) + 128;
	}
	// Helper function to find the next-highest bit of the MSB
	// on the CPU.
	inline uint32_t getHigherMsb(uint32_t n) {
		uint32_t msb = sizeof(n) * 4;
		uint32_t step = msb;
		while (step > 1) {
			step /= 2;
			if (n >> msb)
				msb += step;
			else
				msb -= step;
		}
		if (n >> msb)
			msb++;
		return msb;
	}
